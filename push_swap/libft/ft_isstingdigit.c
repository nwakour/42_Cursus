#include "libft.h"

int		ft_isstingdigit(char *s)
{
	int i;

	i = -1;
	while (s[++i])
	{
		if (s[i] >= '0' && s[i] <= '9')
			continue;
		else
		return (0);
	}
	return (1);
}
